# operasi aritmatika
a = 10
b = 3
c = 5

# operasi tamah +
hasil = a+b
print(a, "+", b, "=", hasil)

# operasi kurang -
hasil = a-b
print(a, "-", b, "=", hasil)

# operasi perkalian *
hasil = a*b
print(a, "*", b, "=", hasil)

# operasi pembagian / (otomatis ke float)
hasil = a/b
print(a, "/", b, "=", hasil)

# operasi eksponen (pangkat)
hasil = a**b
print(a, "**", b, "=", hasil)

# operasi modulus % (sisa di bagi)
hasil = a % b
print(a, "%", b, "=", hasil)

# operasi floor disvision
hasil = a//b
print(a, "//", b, "=", hasil)

hasil = a*b*(c/b)
print(a, '*', b, '*', c, '/', b, "=", hasil)
